# Python trae varios tipos de valores o varios tipos de datos. Estos valores que Python trae se les conoce como tipo primitivos, para diferenciarlos de los tipos que puede crear el mismo programador.
print(type(3)) # int
print(type(3.5)) # float
print(type("hello")) # str
print(type("world")) # str
print(type(True)) # bool
print(type(False)) # bool
print(type("42")) # str